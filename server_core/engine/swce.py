@dataclass
class ProtocolField:
  """A single field within a protocol message."""
  name: str
  offset: int
  size: int
  field_type: str = "unknown"       # unknown, length, type, checksum, data, magic, flags
  endianness: str = "big"
  signed: bool = False
  static_value: Optional[bytes] = None
  entropy: float = 0.0
  confidence: float = 0.0
  description: str = ""


@dataclass
class ProtocolSpec:
  """Structured representation of a reverse-engineered protocol."""
  protocol_name: str = "unknown_protocol"
  transport: str = "tcp"            # tcp, udp
  default_port: int = 0
  endianness: str = "big"
  header_size: int = 0
  fields: List[ProtocolField] = field(default_factory=list)
  length_field: Optional[ProtocolField] = None
  type_field: Optional[ProtocolField] = None
  checksum_field: Optional[ProtocolField] = None
  encryption_indicators: List[Dict[str, Any]] = field(default_factory=list)
  message_types: Dict[int, str] = field(default_factory=dict)
  raw_entropy_map: Dict[int, float] = field(default_factory=dict)
  confidence: float = 0.0
  sample_count: int = 0
  analysis_duration_ms: float = 0.0


@dataclass
class GeneratedTool:
  """Container for a generated protocol tool."""
  tool_id: str = ""
  tool_name: str = ""
  protocol_spec: Optional[ProtocolSpec] = None
  tool_code: str = ""
  language: str = "python"
